    public static boolean isConnected(Context context) {
        NetworkInfo info = getActiveNetworkInfo(context.getApplicationContext());
        return info != null ? info.isConnected() : false;
    }

    public static NetworkInfo getActiveNetworkInfo(Context context) {
        ConnectivityManager cm = (ConnectivityManager) context.getSystemService(Context.CONNECTIVITY_SERVICE);
        return cm.getActiveNetworkInfo();
    }

    public static String getLocalMacAddressFromWifiInfo(Context context) {
        WifiInfo info = getWifiInfo(context);
        return info != null ? info.getMacAddress() : null;
    }

    public static WifiInfo getWifiInfo(Context context) {
        WifiManager wm = (WifiManager) context.getSystemService(Context.WIFI_SERVICE);
        return wm != null ? wm.getConnectionInfo() : null;
    }

    public static String getMacAddress(Context context) {
        String strMac = null;

        if (Build.VERSION.SDK_INT < Build.VERSION_CODES.M) {
            strMac = getLocalMacAddressFromWifiInfo(context);
        } else if (Build.VERSION.SDK_INT < Build.VERSION_CODES.N
                && Build.VERSION.SDK_INT >= Build.VERSION_CODES.M) {
            strMac = getMacAddressInfo(context);
        } else if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.N) {
            if (!TextUtils.isEmpty(getMacAddressInfo(context))) {
                strMac = getMacAddressInfo(context);
            } else if (!TextUtils.isEmpty(getMachineHardwareAddress())) {
                strMac = getMachineHardwareAddress();
            } else {
                strMac = getLocalMacAddressFromBusybox();
            }
        }

        return TextUtils.isEmpty(strMac) ? "02:00:00:00:00:00" : strMac;
    }

